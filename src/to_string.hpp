#ifndef TO_STRING_HPP
#define TO_STRING_HPP

#include <sstream> // ostringstream ?
#include <string>

namespace TEM_NS
{
   template < typename T > std::string to_string( const T& n )
   {
      std::ostringstream ss;
      ss << n;
      return ss.str();
   }
}
#endif
